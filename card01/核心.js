// 计算某年第n个星期一的日期 (用于"快乐星期一"制度)
function getNthMonday(year, month, n) {
  const firstDay = new Date(year, month - 1, 1).getDay();
  const firstMonday = firstDay === 1 ? 1 : (9 - firstDay) % 7 + 1;
  return firstMonday + (n - 1) * 7;
}

// 获取指定年份的简易节假日列表 (硬编码，放弃补假和精确春分秋分)
function getSimpleHolidays(year) {
  return 节假日规则配置.map(规则 => {
    let day = 规则.day;
    if (规则.类型 === '第n个星期一') {
      day = getNthMonday(year, 规则.month, 规则.n);
    }
    return {
      date: new Date(year, 规则.month - 1, day),
      name: 规则.name
    };
  });
}

