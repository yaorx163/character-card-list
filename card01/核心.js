  // 计算单个角色的直接欲望值
  function 计算直接欲望(行为记录, 固有系数) {
    const 欲望 = {
      背德_被动接受: 0,
      背德_主动寻求: 0,
      背德_行为惯性: 0,
      应激情绪_被动接受: 0,
      应激情绪_主动寻求: 0,
      应激情绪_行为惯性: 0,
      责任转移_被动接受: 0,
      责任转移_主动寻求: 0,
      责任转移_行为惯性: 0,
      被欲望验证_被动接受: 0,
      被欲望验证_主动寻求: 0,
      被欲望验证_行为惯性: 0
    };
  
    const 基础增长率 = 1.0; // 全局平衡参数
  
    // 遍历每种行为类型
    for (const [行为类型, 记录数组] of Object.entries(行为记录)) {
      if (!行为影响权重[行为类型]) continue;
  
      // 计算该行为的总刺激量
      const 总刺激量 = (记录数组).reduce((sum, val) => sum + val, 0);
  
      // 获取该行为的权重
      const 权重 = 行为影响权重[行为类型];
  
      // 计算对各个欲望维度的影响
      // 公式：新值 = 旧值 + (行为强度 × 欲望系数 × 模式系数 × 基础增长率)
  
      // 背德相关
      const 背德影响 = 总刺激量 * 权重.背德 * 固有系数.背德系数 * 基础增长率;
      欲望.背德_被动接受 += 背德影响 * 固有系数.被动接受系数;
      欲望.背德_主动寻求 += 背德影响 * 固有系数.主动寻求系数;
      欲望.背德_行为惯性 += 背德影响 * 固有系数.行为惯性系数;
  
      // 应激情绪相关
      const 应激影响 = 总刺激量 * 权重.应激情绪 * 固有系数.应激系数 * 基础增长率;
      欲望.应激情绪_被动接受 += 应激影响 * 固有系数.被动接受系数;
      欲望.应激情绪_主动寻求 += 应激影响 * 固有系数.主动寻求系数;
      欲望.应激情绪_行为惯性 += 应激影响 * 固有系数.行为惯性系数;
  
      // 责任转移相关
      const 责任转移影响 = 总刺激量 * 权重.责任转移 * 固有系数.责任转移系数 * 基础增长率;
      欲望.责任转移_被动接受 += 责任转移影响 * 固有系数.被动接受系数;
      欲望.责任转移_主动寻求 += 责任转移影响 * 固有系数.主动寻求系数;
      欲望.责任转移_行为惯性 += 责任转移影响 * 固有系数.行为惯性系数;
  
      // 被欲望验证相关
      const 被欲望验证影响 = 总刺激量 * 权重.被欲望验证 * 固有系数.被欲望验证系数 * 基础增长率;
      欲望.被欲望验证_被动接受 += 被欲望验证影响 * 固有系数.被动接受系数;
      欲望.被欲望验证_主动寻求 += 被欲望验证影响 * 固有系数.主动寻求系数;
      欲望.被欲望验证_行为惯性 += 被欲望验证影响 * 固有系数.行为惯性系数;
    }
  
    return 欲望;
  }
  
