function simpleHash(str) {
  let hash = 5381;
  for (let i = 0; i < str.length; i++) {
    hash = ((hash << 5) + hash) + str.charCodeAt(i);
  }
  return Math.abs(hash);
}

function 获取季节(dateStr) {
  const date = new Date(dateStr);
  const month = date.getMonth() + 1; // 0-11 -> 1-12
  return 月份季节映射[month];
}

function 随机天气(dateTime, customDistribution = null) {
  const dateStr = new Date(dateTime).toISOString().split('T')[0]
  const hashValue = simpleHash(dateStr);
  const season = 获取季节(dateStr);

  // 使用自定义分布或默认分布
  const distribution = customDistribution || 默认天气分布配置[season];

  // 计算总权重
  const totalWeight = distribution.reduce((sum, w) => sum + w.weight, 0);

  // 生成随机数（基于哈希值，确保相同日期相同结果）
  const randomValue = (hashValue % 10000) / 10000; // 0-0.9999
  const targetWeight = randomValue * totalWeight;

  // 根据权重选择天气
  let accumulatedWeight = 0;
  for (const weather of distribution) {
    accumulatedWeight += weather.weight;
    if (targetWeight < accumulatedWeight) {
      return weather.type;
    }
  }

  // 如果由于浮点数误差没有匹配到，返回第一个
  return distribution[0].type;
}

function 获取温度(dateTime) {
  const date = new Date(dateTime);
  const dateStr = date.toISOString().split('T')[0];
  const hour = date.getHours(); // 获取小时 (0-23)

  const weather = 随机天气(dateStr);
  const season = 获取季节(dateStr);
  const hashValue = simpleHash(dateStr);

  const range = 季节温度范围配置[season];
  const adjustment = 天气温度调整配置[weather] || 天气温度调整配置['default'];

  // 计算日最低温和最高温
  const dailyMin = range.min + adjustment.minAdj;
  const dailyMax = range.max + adjustment.maxAdj;

  // 计算温度变化幅度（根据天气调整）
  const 最小振幅 = 温度计算系数.振幅限制.最小振幅;
  let amplitude = (dailyMax - dailyMin) * adjustment.amplitudeAdj;
  amplitude = Math.max(最小振幅, Math.min(amplitude, dailyMax - dailyMin)); // 限制在合理范围内

  // 重新计算实际的最小值和最大值
  const effectiveMin = dailyMin;
  const effectiveMax = dailyMin + amplitude;

  // 基于日期哈希值生成温度基准（用于确定具体某一天的温度水平）
  const tempSeed = (hashValue % 100) / 100; // 0-1
  const dailyBaseTemp = effectiveMin + tempSeed * (effectiveMax - effectiveMin);

  // 根据小时计算温度变化曲线
  // 使用正弦函数模拟一天中的温度变化，峰值在下午2-3点，谷值在凌晨5-6点
  function getHourlyTemperatureMultiplier(hour) {
    // 将小时转换为弧度 (0-2π)，以凌晨5点为起点
    const hourRad = (hour - 5) * (2 * Math.PI / 24);
    // 使用正弦值从[-1, 1]映射到[0, 1]作为温度系数
    return (Math.sin(hourRad) + 1) / 2;
  }

  // 获取小时温度系数
  let hourlyMultiplier = getHourlyTemperatureMultiplier(hour);
  let curveAdjustment = 0;

  // 根据季节调整曲线形状
  switch (season) {
    case 'summer':
      // 夏季夜间温度也较高
      if (hour >= 22 || hour < 6) {
        hourlyMultiplier = Math.max(温度计算系数.夏季.夜间最低系数, hourlyMultiplier);
      }
      curveAdjustment = 温度计算系数.夏季.曲线调整;
      break;
    case 'winter':
      // 冬季温度变化相对平缓
      if (hour >= 22 || hour < 6) {
        hourlyMultiplier = Math.min(温度计算系数.冬季.夜间最高系数, hourlyMultiplier);
      }
      curveAdjustment = 温度计算系数.冬季.曲线调整;
      break;
    case 'spring':
    case 'autumn':
      // 春秋季温度变化适中
      if (hour >= 22 || hour < 6) {
        hourlyMultiplier = Math.max(温度计算系数.春秋季.夜间最低系数, Math.min(温度计算系数.春秋季.夜间最高系数, hourlyMultiplier));
      }
      curveAdjustment = 温度计算系数.春秋季.曲线调整;
      break;
  }

  // 计算最终温度
  let temperature = dailyBaseTemp;

  // 根据小时调整温度
  if (hour >= 6 && hour <= 18) {
    // 白天：温度随太阳升高
    temperature += (hourlyMultiplier - 0.5) * amplitude;
  } else {
    // 夜间：温度随太阳降低，变化幅度较小
    temperature += (hourlyMultiplier - 0.5) * amplitude * 温度计算系数.夜间振幅衰减;
  }

  // 加上曲线调整
  temperature += curveAdjustment * amplitude;

  // 根据天气的特殊调整
  if (weather.includes('雨') || weather.includes('雪')) {
    if (hour >= 18 || hour < 6) {
      // 雨雪天气夜间降温不明显
      temperature += 温度计算系数.雨雪天气.夜间升温;
    } else {
      // 雨雪天气白天升温不明显
      temperature += 温度计算系数.雨雪天气.白天降温;
    }
  }

  // 确保温度在合理范围内
  const 最终下限 = effectiveMin - 温度计算系数.振幅限制.下限容差;
  const 最终上限 = effectiveMax + 温度计算系数.振幅限制.上限容差;
  temperature = Math.max(最终下限, Math.min(最终上限, temperature));

  return Math.round(temperature) + '°C';
}

