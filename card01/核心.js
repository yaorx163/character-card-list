// 提取的函数2：获取下次节假日
const 获取下一个节假日 = (dateString) => {
  const currentDate = new Date(dateString);
  const todayStart = new Date(currentDate.getFullYear(), currentDate.getMonth(), currentDate.getDate());
  
  // 获取今年和明年的节日，防止跨年查不到
  const holidays = [
    ...getSimpleHolidays(currentDate.getFullYear()),
    ...getSimpleHolidays(currentDate.getFullYear() + 1)
  ];

  for (const holiday of holidays) {
    if (holiday.date >= todayStart) {
      const diffTime = holiday.date.getTime() - todayStart.getTime();
      const daysUntil = Math.round(diffTime / (1000 * 60 * 60 * 24));
      return `${holiday.name}(${daysUntil}天后)`;
    }
  }
  
  return "未知";
};

